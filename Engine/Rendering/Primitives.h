////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef ENGINE_RENDERING_PRIMITIVES_H_INCLUDED
#define ENGINE_RENDERING_PRIMITIVES_H_INCLUDED

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "Common/Typedefs.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace Engine
{
namespace Rendering
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Position;
struct Diffuse_color;
struct Texture_coord;
enum Blending_mode;
enum Vertex_format;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Position
{
   float x;
   float y;
   float z;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Diffuse_color
{
   uchar a;
   uchar r;
   uchar g;
   uchar b;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Texture_coord
{
   float tu;
   float tv;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/// Color and alpha blending modes; used promarily for texturing.
enum Blending_mode
{
   blending_mode_select_arg1
   , blending_mode_select_arg2
   , blending_mode_modulate
   , blending_mode_add
   , blending_mode_disable

   , blending_mode_number_of_elements
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/// Vertex format that define vertex meaning.
enum Vertex_format
{
   position             = 0x01
   , diffuse_color      = 0x02
   , texture_coord0     = 0x10
   , texture_coord1     = 0x20
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

} // namespace Rendering
} // namespace Engine

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#endif // ENGINE_RENDERING_PRIMITIVES_H_INCLUDED

